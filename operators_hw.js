//Arithmetic Operators
const num1=16;
const num2=4;
console.log(`The addition of ${num1} and ${num2} is: ${num1+num2}`);
console.log(`The subtraction of ${num1} and ${num2} is: ${num1-num2}`);
console.log(`The multiplication of ${num1} and ${num2} is: ${num1*num2}`);
console.log(`The division of ${num1} and ${num2} is: ${num1/num2}`);
console.log(`The value of ${num1} to the power ${num2} is: ${num1**num2}`);
console.log(`The modulus of ${num1} and ${num2} is: ${num1%num2}`);

//Incrementation and Decrementation
var x=5;
var y=8;
console.log(`Value of ${x} after incrementation is: ${x++}`);
console.log(`Value of ${y} after decrementation is: ${--y}`);

//Comparision Operators
const num3=10;
const num4=20;
console.log(`${num3} is greater than ${num4}: ${num3>num4}`);
console.log(`${num3} is less than ${num4}: ${num3<num4}`);
console.log(`${num3} is equal to ${num4}: ${num3==num4}`);
console.log(`${num3} is greater than or equal to ${num4}: ${num3>=num4}`);
console.log(`${num3} is less than or equal to ${num4}: ${num3<=num4}`);
console.log(`${num3} is not equal to ${num4}: ${num3!=num4}`);

//Comparing two different data types
const a='4';
const b=4;
console.log(`${a} is equal to ${b}: ${a==b}`);
console.log(`${a} is equal to ${b}: ${a===b}`);
console.log(`${a} is not equal to ${b}: ${a!=b}`);
console.log(`${a} is not equal to ${b}: ${a!==b}`);

//TypeOf Operator
